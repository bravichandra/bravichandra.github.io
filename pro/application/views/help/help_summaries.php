<p><strong>Explanation</strong><br />
  You have provided details  around your products/services and outlined the different benefits that are  provided. Since we often won't have the time to go into all of those details  when talking with prospects, we are going to use these field to try to  summarize and group some of the points together so that we get to a short list  of powerful points that summarize what you sell and the value that you offer. </p>
<p>If there is not a way to  summarize the points, pick one that is most important or dominant and enter  that. These summary points are important and probably show up in the scripts  more than the individual points.</p>
