<p><strong>Explanation</strong><br />
  There may be some facts or  details about your company or products that could be used to grab the  prospect's attention and interest. We already outlined how you differ. Now we  are looking for company facts or details that might be impressive or bragging points.</p>
<p>Things to look at in this  area are years in business, awards that have been one, the size or focus of the  company, etc.</p>
<p><strong>Examples</strong><br />
  we are a local provider  and have been in business for 30 years<br />
  we have won the Gumby  customer service award for the past 7 years<br />
  our solutions are built by  sales people built for sales people</p>
