<p class="space"><strong>Explanation</strong><br />
  When talking with  prospects for the first time, it can help to have clarity as to what the best  next step and type of meeting to drive the prospect to. Is the best first  meeting a presentation, a simply conversation, a discovery discussion,  etc.</p>
<p>Simply having this step  identified can help to improve focus and execution. Try to identify what the  best first meeting is for you in terms of type of meeting an format.</p>
<p><strong>Examples</strong><br />
  face-to-face meeting<br />
  online demonstration<br />
  discovery discussion</p>
