  <p><strong>Explanation</strong><br />
  When one of your products  or services is used by a client, there are likely some sort of benefits or  improvements achieved. These benefits can occur at a few different levels. As  we try to identify what improvements or benefits you have to offer, we start at  the lowest and most technical level. </p>
  <p>When a prospect uses one  of your products or services, what improvements might they see in the areas of processes,  systems, people (employees), etc? Try to answer in a few words or less.<br />
    <br />
    <strong>Examples</strong><br />
    improve system uptime<br />
    improve the scheduling of  employees<br />
    improve the management of  projects</p>
