<p><strong>Explanation</strong><br />
  When a prospect realizes  improvements at the technical level, these will typically work their way up to  create benefits at the business level and these can typically be seen in the  areas of revenue, costs, delivery of services, customers, etc. </p>
<p>When your clients use your  products and services and they realize technical improvements, how does that  help them at a business level? Try to answer in a few words or less.</p>
<p><strong>Examples</strong><br />
  more reliable service  delivery<br />
  better control and able to  decrease labor costs<br />
  improve ability to meet  project budget and time targets</p>
