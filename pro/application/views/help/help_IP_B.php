<p><strong>Explanation</strong><br />
  The real power of starting  to think about your ideal prospect comes when you start to identify what they  look like from a current environment perspective. </p>
<p>You have identified the  pains that you resolve. You could stop right there by saying that your ideal  prospect will have all of those pains. But your knowledge goes to the next  level when you go one step further to try to identify what typically causes the  pain that you resolve. </p>
<p>For example, an eye doctor  will help people to get rid of headaches. The headaches are the pain that the  doctor resolves. But the headaches that the doctor can help with are caused by  poor vision and not many other causes. Identifying this type of detail really  helps to develop the picture for your ideal prospect.</p>
<p><strong>Examples</strong><br />
  losing customers to the  competition due to poor service<br />
  do not have an effective  labor management system<br />
  having to pay unplanned  costs due to project overruns </p>
