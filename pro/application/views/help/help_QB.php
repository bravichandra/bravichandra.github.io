<p><strong>Explanation</strong><br />
  Once we have identified  the key details or characteristic that that our ideal prospects should have, we  can then develop questions to ask the prospect to identify if they have any of  these. For example, if our ideal prospect currently has a manual expense  reporting process, we may want to ask prospects that we talk to if they are  processes expenses reports manually or if they have a system.</p>
<p><strong>Examples</strong><br />
  How concerned are you  about losing customers due to poor service delivery?<br />
  How do you feel about your  current labor management system?<br />
  How do you feel about your  ability to complete projects on time and on budget?</p>
