<p><strong>Explanation</strong><br />
  One way to build interest  is to communicate what could potentially happen or be at risk if the prospect  does not purchase from you. </p>
<p>A prime example of this is  a prospect that is considering purchasing flood insurance. The prospect could  hold off on the purchase, but if there is a flood, the prospect could lose  their home. That is the risk or potential impact from doing nothing.</p>
<p><strong>Examples</strong><br />
  high potential for  accidents and safety concerns<br />
  missed opportunities and  lost customers from not managing information appropriately<br />
  high customer acquisition  costs impacting the bottom line</p>
