<p><strong>Explanation</strong><br />
  We have identified how you  help at a business level. When you provide those improvements, or when those  improvements are needed, what are the challenges or pains that the prospect is  experiencing. This step helps you to identify what problems or pains you  resolve by looking at how you help. </p>
<p>What business pains or  challenges are resolved by the improvements that you help to create? Try to  answer in a few words or less.</p>
<p><strong>Examples</strong><br />
  not able to deliver  consistent service to customers<br />
  not able to contol labor  costs<br />
  not able to manage project  budgets and funds</p>
