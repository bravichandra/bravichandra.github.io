<p><strong>Explanation</strong><br />
  Many sales people sell to  different types of prospects. For many, just saying that you sell to  &ldquo;businesses&rdquo; will usually work just fine. But in some cases, that does not fit  as there is either a very particular type of business that should be specified  or the label of &ldquo;business&rdquo; does not make sense at all. This is important as it  feeds into the scripts when you talk about what kinds of organizations that you  help. </p>
<p><strong>Examples</strong><br />
  businesses (most common)<br />
  small businesses<br />
  doctors<br />
  law firms</p>
